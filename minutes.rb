# Define constants for the number of minutes in an hour, hours in a day, days in a year, and years in a decade
minutes_in_an_hour = 60
hours_in_a_day = 24
days_in_a_year = 365
years_in_a_decade = 10

# Calculate the number of minutes in a decade
minutes_in_a_decade = minutes_in_an_hour * hours_in_a_day * days_in_a_year * years_in_a_decade

# Print the result
puts "There are #{minutes_in_a_decade} minutes in a decade."